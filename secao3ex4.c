#include<stdio.h>

int main(){
    //Declaração de Variáveis
    int num1, num2, soma;

    //Entradas
    printf("\nInforme o primeiro numero: ");
    scanf("%d", &num1);

    printf("\nInforme o segundo numero: ");
    scanf("%d", &num2);

    //Processamento
    soma = num1+ num2;

    //saida
    printf("\nA soma de %d + %d eh igual a %d",num1, num2, soma);






}
